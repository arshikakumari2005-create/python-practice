# operators in python
# Arithmetic operators
# +  Addition
# -  subtraction 
# *  mutiplication
# /  division
# // floor division
# %  modulus(reminder)
# ** power

num = 24
num2= 15
print(int(num2) + float(num))
print(num + num2)
print(num - num2 )
print(num * num2)
print(num ** 2)
print(num / num2)
print(num // num2)
print(num % 5)

